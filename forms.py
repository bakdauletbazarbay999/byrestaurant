def get_hyundai_ai_response(prompt: str) -> str:
    """
    Gemini моделін пайдаланып, Hyundai автосалон кеңесшісінің жауабын алады.
    """
    if not client:
        return "⚠ Жүйелік қате: Gemini API Client инициализацияланбаған. API кілтін тексеріңіз."

    try:
        # Gemini моделіне конфигурация
        config = types.GenerateContentConfig(
            system_instruction=SYSTEM_INSTRUCTION_HYUNDAI
        )

        # Gemini-ге сұраныс жіберу
        # Бұрынғыдай бірнеше моделді қолданудың орнына, тұрақты модельді пайдаланамыз:
        response = client.models.generate_content(
            model='gemini-2.5-flash',
            contents=prompt,
            config=config,
        )

        if response.text:
            return response.text.strip()
        else:
            return "Кешіріңіз, мен сұрағыңызға жауап бере алмадым. Сұрақты нақтылап көріңіз."

    except Exception as e:
        # Кез келген жүйелік қателерді ұстау
        return f"❌ Жүйелік қате: Сұранысты өңдеуде қате шықты. Қате: {str(e)}"